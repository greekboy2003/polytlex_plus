    private void jComboBox_Language_ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox_Language_ActionPerformed
        
        String lang=(String)jComboBox_Language_.getSelectedItem();
        //System.out.println("Test");
        //System.out.println(lang);
        
        if (lang == "Deutsch")
        {   
            setMyLang="German";
            setMyLangChars="ger";
            icon = new ImageIcon("./images/" + setMyLang + ".png");
            jLabel_Flag_Lang.setIcon(icon);
           
        }
        
        if (lang == "English")
        {
            setMyLang="English";
            setMyLangChars="en";
            icon = new ImageIcon("./images/" + setMyLang + ".png");
            jLabel_Flag_Lang.setIcon(icon);
            
        }
            
        if (lang == "Español")
        {   
            setMyLang="Spanish";
            setMyLangChars="sp";
            icon = new ImageIcon("./images/" + setMyLang + ".png");
            jLabel_Flag_Lang.setIcon(icon);
           
        }
                
        if (lang == "Français")
        {   
            setMyLang="French";
            setMyLangChars="fr";
            icon = new ImageIcon("./images/" + setMyLang + ".png");
            jLabel_Flag_Lang.setIcon(icon);
           
        }        
        
        if (lang == "Italiano")
        {   
            setMyLang="Italian";
            setMyLangChars="it";
            icon = new ImageIcon("./images/" + setMyLang + ".png");
            jLabel_Flag_Lang.setIcon(icon);
          
        }

         if (lang == "Русский")
        {   
            setMyLang="Russian";
            setMyLangChars="ru";
            icon = new ImageIcon("./images/" + setMyLang + ".png");
            jLabel_Flag_Lang.setIcon(icon);
           
        }
   
        if (lang == "Ἑλληνικὰ")
        {
            setMyLang="Greek";
            setMyLangChars="gr";
            icon = new ImageIcon("./images/" + setMyLang + ".png");
            jLabel_Flag_Lang.setIcon(icon);
           
        }  
            
        if (lang == "中國傳統")   //Traditional
        {   
            setMyLang="Chinese (traditional)";
            setMyLangChars="chtr";
        }
                
        if (lang == "简体中文")  //Simplified
        {   
            setMyLang="Chinese (simplified)";
            setMyLangChars="chsim";
        }
        
                       
        try {
            FileWriter fwMyLang = new FileWriter("./settings/mylang.set");
            fwMyLang.write(setMyLang + "\n");
            fwMyLang.write(setMyLangChars + "\n");
            fwMyLang.close();

            }    
            
        catch (IOException e) {
                    e.printStackTrace();
            }
                
    }//GEN-LAST:event_jComboBox_Language_ActionPerformed

